using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Library.Models
{
    /// <summary>
    /// Модел за автор на публикации
    /// Автор на кода: Божидар Георгиев (136ikz@unibit.bg)
    /// </summary>
    public class Author
    {
        /// <summary>
        /// Уникален идентификатор на автора
        /// </summary>
        public int Id { get; set; }

        /// <summary>
        /// Име на автора (пълно име)
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// ID на държавата на автора
        /// Указва националността или държавата, от която е авторът
        /// </summary>
        public int CountryId { get; set; }

        /// <summary>
        /// Биография на автора
        /// Съдържа информация за живота и творчеството на автора
        /// </summary>
        public string Biography { get; set; }

        // ========================================================================================================
        // НАВИГАЦИОННИ СВОЙСТВА
        // ========================================================================================================

        /// <summary>
        /// Държава на автора (навигационно свойство)
        /// </summary>
        public Country Country { get; set; }

        /// <summary>
        /// Колекция от връзки с библиотечни артикули
        /// Реализира "много към много" връзка между автори и артикули
        /// Един автор може да има много публикации
        /// </summary>
        public ICollection<ItemAuthor> ItemAuthors { get; set; }
    }
}
